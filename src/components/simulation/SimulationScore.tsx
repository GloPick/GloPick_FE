import { Briefcase, MapPin } from 'lucide-react';

interface SimulationScoreProps {
  title: string;
  value: number;
  type: 'employment' | 'migration';
}

const SimulationScore = ({ title, value, type }: SimulationScoreProps) => {
  const colorClass = type === 'employment' ? 'text-blue-500' : 'text-yellow-500';
  const bgClass = type === 'employment' ? 'bg-blue-100/50' : 'bg-yellow-100/50';

  const icon = type === 'employment' ? <Briefcase size={18} /> : <MapPin size={18} />;

  return (
    <div
      className={`w-full rounded-xl p-4 ${bgClass} flex flex-col items-center text-center shadow-sm`}
    >
      <div className="flex items-center gap-2 mb-2 text-md text-gray-700 font-semibold">
        {icon}
        {title}
      </div>
      <div className={`text-3xl font-bold ${colorClass}`}>{value}%</div>
    </div>
  );
};

export default SimulationScore;
